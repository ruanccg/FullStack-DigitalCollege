// Leia base e altura de um triângulo e calcule sua área
// (Área do triângulo: b * h / 2)

let base = Number(prompt("Qual a base do seu trinângulo?"));
let altura = Number(prompt("Qual a altura do seu triângulo?"));

let calculoArea = (base * altura) / 2;

console.log(`A área do seu trinângulo é: ${calculoArea}`);